package datatype_variables1;

public class original_2
{

	    //. Variable declaration (Allocating/Reserving memory)
	public static void main(String[] args)      
	{
		String studentName;
		int studentrollNo;         //datatype variableName
		float studentper;
		char studentgrade;
		
		
		//2. Variable Initialization(Assigning or Inserting value)
		studentName= "Veera";
		studentrollNo= 96;           //variableName = "variable value";
		studentper= 97.88f;
		studentgrade= 'A';
		
		
		//3: variable usage
		System.out.println(studentName);
        System.out.println(studentrollNo);     //info reuse
        System.out.println(studentper);
        System.out.println(studentgrade);
        
		
		
	}
}
